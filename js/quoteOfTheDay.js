// Функция для выбора случайной фразы
function getQuoteOfTheDay() {
    const randomIndex = Math.floor(Math.random() * quotes.length);
    return quotes[randomIndex];
}

// Функция для отображения фразы дня на странице
function displayQuoteOfTheDay() {
    const quoteElement = document.getElementById('quote-of-the-day');
    const authorElement = document.getElementById('quote-author');
    const quote = getQuoteOfTheDay();
    quoteElement.textContent = `"${quote.text}"`;
    authorElement.textContent = `- ${quote.author}`;
}

// Вызываем функцию для отображения фразы дня при загрузке страницы
window.onload = displayQuoteOfTheDay;
